const inputValue = ref("https://github.com/untrustedmodders/plugify-source-2/blob/main/s2sdk.pplugin.in");

FetchAndParseJSON(ConvertToRawUrl(inputValue.value))
    .then((data: ManifestType) => {
      if (data.exportedMethods === undefined) {
        return
      }

      for (let method of data.exportedMethods) {
        ProcessMethod(method, delgs.value, enums.value);
      }

      methods.value = data.exportedMethods.reduce((acc: any, item: MethodType) => {
        const group = item.group || data.friendlyName || "";
        if (!acc[group]) {
          acc[group] = [];
        }
        acc[group].push(item);
        return acc;
      }, {});
    })
    .catch((error) => console.error('Error:', error));

